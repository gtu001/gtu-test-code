package gtu.digester;

public class JNDIProvider {
    private String name;
    private String value;

    public JNDIProvider() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
